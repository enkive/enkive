package com.linuxbox.enkive.message;

public class ContentException extends Exception {
	private static final long serialVersionUID = -8311161737944025643L;

	public ContentException() {
		super();
	}

	public ContentException(String arg0) {
		super(arg0);
	}

	public ContentException(Throwable arg0) {
		super(arg0);
	}

	public ContentException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
